# แลกเงินและหาจำนวนแบงค์
# 2000 => 1000 2 ใบ
# 1500 => 1000 1 ใบ, 500 1 ใบ
# 1800 => 1000 1 ใบ, 500 1 ใบ, 100 3 ใบ
# 100 => 100 1 ใบ

# 2000%1000 = 0
# 1500%1000 = 500
# 1500%500 = 0
# 1800%1000 = 800
# 1800%500 = 300
# 1800%100 = 0

num = int(input("ป้อนจำนวนเงินของคุณ : "))

print("1000 บาท =",num//1000,"ใบ")
print("500 บาท =",(num%1000)//500,"ใบ")
print("100 บาท =",(num%500)//100,"ใบ")
print("50 บาท =",(num%100)//50,"ใบ")
print("20 บาท =",(num%50)//20,"ใบ")
